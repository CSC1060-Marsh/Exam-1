public class PrintNumber {
    public static void main(String[] args) {
        //This creates the variable "number".
        int number;
        //This starts a "for" loop, starting the number at 5, adding one, executing until the number is greater than 19.
        for (number = 5; number <= 19; number++) {
            //This prints out the number on a new line.
            System.out.println(number);
            }
    }
}
